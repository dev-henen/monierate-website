	<span class="block mb-4">
		<h3>What is Buy Price?</h3>
		<p>
			The buy price is the rate at which you pay to convert naira to dollars. It represents how much
			naira you're paying for every dollar obtained. For instance, if you have naira but need USDT
			to pay a bill, you exchange your naira for USDT, and the provider credits USDT to you at the
			buy price.
		</p>
	</span>
	<span class="block mb-4">
		<h3>What is Sell Price?</h3>
		<p>
			The sell price is the rate at which you pay to convert dollars to naira. It represents how
			much naira you're getting for every dollar exchanged. For instance, if you have USDT but need
			naira to pay a bill, you exchange your USDT for naira, and the provider credits naira to you
			at the sell price.
		</p>
	</span>
	<span class="block mb-4">
		<h3>Why is the buy price usually higher than the sell price?</h3>
