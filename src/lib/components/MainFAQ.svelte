		<h2>What is a Provider?</h2>
		<p>
			A provider is an entity that helps facilitate the exchange of currencies. If you have 100 USDT
			and want to exchange it for naira, you will need an exchange provider to convert your USDT to
			naira. A provider can be an individual or entity. On Monierate, we only list trusted entities
			for you to choose from, not individual traders.
		</p>
